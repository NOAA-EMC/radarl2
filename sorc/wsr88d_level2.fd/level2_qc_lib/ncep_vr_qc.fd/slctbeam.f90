      Subroutine slctbeam(indxint,athv,azim,nobs)

      implicit none

!#######################################################################
!
!     Variable Declarations.
!
!#######################################################################

   ! parameter for observation
      integer             ::nobs,indxint
      real                ::athv
      real,dimension(nobs)::azim,drobs
      integer,dimension(1)::min_pos

      real                ::spval

!@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@
!
!     Beginning of executable code...
!
!@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@

         drobs=abs(azim-athv)
         min_pos=minloc(drobs)
         indxint=min_pos(1)

      End
